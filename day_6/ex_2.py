from collections import deque

def solution(lanternfish):
	timers = [0]*9

	for item in lanternfish:
		timers[item] += 1
		
	timers = deque(timers)
	for day in range(0, 256):
		if timers[0] > 0:
			tmp = timers[0]
			timers[0] = 0
			timers.rotate(-1)
			timers[6] += tmp
			timers[8] += tmp
		else:
			timers.rotate(-1)

	return sum(timers)

